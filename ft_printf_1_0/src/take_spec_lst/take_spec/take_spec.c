#include <stdlib.h>
#include "ft_printf.h"


char	*take_spec(char *s, t_spec_info *lst)
{
	if (*s != '\0' && *s == '%')
		s++;
	while (*s != '\0' && is_flag(*s))
		s++;
	lst->spec = (*s);
	return (++s);
}
